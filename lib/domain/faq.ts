/** 검색 — 질문·본문·표·태그 전부 훑는다 */
export function matchFaq(it: FaqItem, q: string): boolean {
  const s = q.trim().toLowerCase();
  if (!s) return true;
  const hay = [
    it.q,
    ...(it.a || []),
    ...(it.rows || []).flat(),
    it.tags || '',
  ].join(' ').toLowerCase();
  return hay.includes(s);
}
